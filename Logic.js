function formatBytes(bytes) { if (bytes < 1024) return bytes + ' B'; if (bytes < 1048576) return (bytes/1024).toFixed(1) + ' KB'; return (bytes/1048576).toFixed(2) + ' MB'; }
function estimateJSONSize(obj) { try { return new Blob([JSON.stringify(obj)]).size; } catch(e) { return JSON.stringify(obj).length; } }

function estimateSystemJSSize() {
    const sysData = { adminHash: globalAdminHash, salt: globalSalt, siteConfig: currentConfig, authToken: globalAuthToken };
    const sysB64 = unicodeToBase64(JSON.stringify(sysData));
    const postsB64 = unicodeToBase64(JSON.stringify(postsCache));
    const comicsB64 = unicodeToBase64(JSON.stringify(comicsCache));
    return {
        raw: new Blob([JSON.stringify(sysData)]).size,
        encoded: new Blob(['window.AZUMINT_SYSTEM = "' + sysB64 + '";']).size,
        postsEncoded: new Blob(['window.AZUMINT_POSTS = "' + postsB64 + '";']).size,
        comicsEncoded: new Blob(['window.AZUMINT_COMICS = "' + comicsB64 + '";']).size,
        posts: estimateJSONSize(postsCache), comics: estimateJSONSize(comicsCache),
        config: estimateJSONSize(currentConfig), interactions: estimateJSONSize(interactionsCache),
        postCount: postsCache.length, comicCount: comicsCache.length, interactionKeys: Object.keys(interactionsCache).length
    };
}

