function sanitizeBeforeSave() {
    let stripped = 0;
    postsCache.forEach(p => {
        if (p.content && p.content.includes('data:image/')) { const b = p.content.length; p.content = p.content.replace(/data:image\/[a-z+]+;base64,[A-Za-z0-9+/=]+/g, '[image]'); if (p.content.length < b) stripped++; }
        if (p.media === undefined || p.media === null) delete p.media;
    });
    if (stripped > 0) console.log('[Sanitizer] Stripped ' + stripped + ' inline data URI(s)');
    return stripped;
}

/* ═════════════════════════════════════════════
   UPLOAD TO BRIDGE — applesaucy pattern exactly
   ═════════════════════════════════════════════ */

function uploadToBridge(target, mediaFile = null, explicitMediaPath = null, overrideToken = null) {
    const tokenToSend = overrideToken || globalAuthToken;
    if (!tokenToSend) { showToast('No Auth Token found. Run setup or repair.', 'error'); return; }

    let passwordCheck = null;
    // ALL file uploads need passwordCheck — comics and interactions included
    if (target === 'system' || target === 'posts' || target === 'comics' || target === 'interactions' || target === 'media_only') {
        if (!sessionPassword) {
            // Visitors liking/commenting have no sessionPassword — silently skip upload, don't spam repair modal
            if (target === 'interactions') return;
            showToast('Session expired. Please re-login.', 'error'); return;
        }
        passwordCheck = sessionPassword;
    }

    let fileContent = '', fileName = '', mediaName = null;

    if (target === 'system') {
        const data = { adminHash: globalAdminHash, salt: globalSalt, siteConfig: currentConfig, authToken: tokenToSend, apiKey: null };
        fileContent = 'window.AZUMINT_SYSTEM = "' + unicodeToBase64(JSON.stringify(data)) + '";';
        fileName = 'system.js';

    } else if (target === 'posts') {
        sanitizeBeforeSave();
        fileContent = 'window.AZUMINT_POSTS = "' + unicodeToBase64(JSON.stringify(postsCache)) + '";';
        fileName = 'posts.js';

    } else if (target === 'comics') {
        fileContent = 'window.AZUMINT_COMICS = "' + unicodeToBase64(JSON.stringify(comicsCache)) + '";';
        fileName = 'comics.js';
        showToast('Syncing comics…', 'loading');

    } else if (target === 'interactions') {
        fileContent = 'window.AZUMINT_INTERACTIONS = "' + unicodeToBase64(JSON.stringify(interactionsCache)) + '";';
        fileName = 'interactions.js';
        showToast('Syncing interactions...', 'loading');

    } else if (target === 'media_only') {
        // Pure file upload — no .js content, just the binary file
        // Neocities API creates img/ automatically when path prefix is img/
        fileContent = '';
        fileName = '';
        if (mediaFile) {
            const rawName = explicitMediaPath || mediaFile.name.replace(/\s+/g, '-');
            mediaName = (rawName && rawName.indexOf('/') === -1) ? 'img/' + rawName : rawName;
        }
    }

    // For non-media_only targets that include a media file (legacy compat)
    if (mediaFile && target !== 'media_only') {
        const rawName = explicitMediaPath || mediaFile.name.replace(/\s+/g, '-');
        mediaName = (rawName && rawName.indexOf('/') === -1) ? 'img/' + rawName : rawName;
    }

    const frame = document.getElementById('bridge-frame');
    if (frame && frame.contentWindow) {
        frame.contentWindow.postMessage({ type: 'UPLOAD', authToken: tokenToSend, passwordCheck, fileContent, filename: fileName, mediaFile: mediaFile, mediaName: mediaName }, FIXED_BRIDGE_URL);
    } else showToast('Bridge disconnected. Try refreshing.', 'error');
}

// Direct image upload for settings fields (pfp, banner, bg, etc.) — applesaucy pattern
