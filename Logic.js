function renderMediaQueue(mode) {
    const containerId = mode === 'edit' ? 'edit-media-queue' : 'admin-media-queue';
    const container = document.getElementById(containerId);
    if (!container) return;
    if (!currentPostMediaQueue.length) { container.innerHTML = ''; return; }
    container.innerHTML = currentPostMediaQueue.map((item, idx) => {
        let prevHtml = '', name = '';
        if (item.type === 'file') {
            name = item.val.name;
            if (item.preview) {
                prevHtml = '<img class="media-preview" src="' + item.preview + '" alt="">';
            } else if (item.val.type && item.val.type.startsWith('video/')) {
                prevHtml = '<div class="media-preview" style="display:flex;align-items:center;justify-content:center;background:var(--bg-elevated);font-size:1.2rem;">&#9654;</div>';
            } else {
                prevHtml = '<div class="media-preview" style="display:flex;align-items:center;justify-content:center;background:var(--bg-elevated);font-size:0.6rem;color:var(--gray);">FILE</div>';
            }
        } else {
            name = item.val;
            if (item.val && item.val.match(/\.(jpeg|jpg|gif|png|webp)$/i)) {
                prevHtml = '<img class="media-preview" src="' + item.val + '" alt="" onerror="this.style.opacity=0.2">';
            } else if (item.val && item.val.match(/\.(mp4|webm|mov)$/i)) {
                prevHtml = '<div class="media-preview" style="display:flex;align-items:center;justify-content:center;background:var(--bg-elevated);font-size:1.2rem;">&#9654;</div>';
            } else {
                prevHtml = '<div class="media-preview" style="display:flex;align-items:center;justify-content:center;background:var(--bg-elevated);font-size:0.6rem;color:var(--gray);">URL</div>';
            }
        }
        return '<div class="media-queue-item" data-idx="' + idx + '">'
            + prevHtml
            + '<span class="media-info" title="' + escapeHtml(name) + '">' + escapeHtml(name) + '</span>'
            + '<div class="queue-controls">'
            + (idx > 0 ? '<button class="queue-btn" onclick="moveQueueItem(' + idx + ',-1,\'' + mode + '\')" title="Move up">&uarr;</button>' : '')
            + (idx < currentPostMediaQueue.length - 1 ? '<button class="queue-btn" onclick="moveQueueItem(' + idx + ',1,\'' + mode + '\')" title="Move down">&darr;</button>' : '')
            + '<button class="queue-btn remove" onclick="removeQueueItem(' + idx + ',\'' + mode + '\')" title="Remove">&times;</button>'
            + '</div></div>';
    }).join('');
}

