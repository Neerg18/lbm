function reactTo(postId, type) {
    if (!interactionsCache[postId]) interactionsCache[postId] = { likes: 0, dislikes: 0, comments: [] };
    let userReactions = {}; try { userReactions = JSON.parse(localStorage.getItem(LOCAL_REACTIONS_KEY) || '{}'); } catch(e) {}
    const current = userReactions[postId];
    if (current === type) { interactionsCache[postId][type + 's'] = Math.max(0, (interactionsCache[postId][type + 's'] || 0) - 1); delete userReactions[postId]; showToast('Vote removed'); }
    else { if (current) interactionsCache[postId][current + 's'] = Math.max(0, (interactionsCache[postId][current + 's'] || 0) - 1); interactionsCache[postId][type + 's'] = (interactionsCache[postId][type + 's'] || 0) + 1; userReactions[postId] = type; showToast('Vote recorded!'); }
    localStorage.setItem(LOCAL_REACTIONS_KEY, JSON.stringify(userReactions));
    renderLeaflets(); saveInteractionsLocal();
}

