let tokenizeResolve = null;

function requestTokenGeneration(key, pass) {
    return new Promise(resolve => {
        tokenizeResolve = resolve;
        const frame = document.getElementById('bridge-frame'); if (!frame) { resolve(null); return; }
        frame.contentWindow.postMessage({ type: 'TOKENIZE', rawKey: key, adminPass: pass }, FIXED_BRIDGE_URL);
        setTimeout(() => { if (tokenizeResolve) tokenizeResolve(null); }, 10000);
    });
}

function handleBridgeMessage(e) {
    if (e.data.type === 'TOKEN_RESULT') {
        if (e.data.success && tokenizeResolve) tokenizeResolve(e.data.token);
        else if (!e.data.success && tokenizeResolve) { showToast('Token Generation Failed: ' + e.data.message, 'error'); tokenizeResolve(null); }
        tokenizeResolve = null;
    }

    if (e.data.type === 'UPLOAD_RESULT') {
        // Batch upload path — resolve/reject the promise and return
        if (isBatchUploading) {
            if (e.data.success) {
                if (currentBatchResolve) currentBatchResolve(true);
            } else {
                if (currentBatchReject) currentBatchReject(e.data.message);
            }
            return;
        }

        // Normal save path
        if (currentLoadingToast && currentLoadingToast.parentNode) currentLoadingToast.remove();
        if (e.data.success) showToast('✓ Synced!', 'success');
        // Only show repair modal to the admin — never to regular visitors
        else if (isAdminLoggedIn && e.data.message && (e.data.message.includes('Invalid') || e.data.message.includes('403') || e.data.message.includes('invalid credentials'))) document.getElementById('repair-modal').style.display = 'flex';
        else if (!isAdminLoggedIn && e.data.message && e.data.message.includes('403')) { /* silently ignore auth errors for public visitors */ }
        else showToast('Sync failed: ' + (e.data.message || 'Unknown error'), 'error');
    }
}

// Promise wrapper for a single file upload — applesaucy pattern
function promisifiedUpload(mediaFile, path) {
    return new Promise((resolve, reject) => {
        currentBatchResolve = resolve;
        currentBatchReject = reject;
        uploadToBridge('media_only', mediaFile, path);
        setTimeout(() => {
            if (currentBatchResolve) { currentBatchResolve = null; currentBatchReject = null; reject('Upload timed out: ' + mediaFile.name); }
        }, 30000);
    });
}

