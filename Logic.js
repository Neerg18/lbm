function parseSystemData() {
    if (typeof window.AZUMINT_INTERACTIONS !== 'undefined') {
        try { interactionsCache = JSON.parse(base64ToUnicode(window.AZUMINT_INTERACTIONS)); localStorage.setItem('NEERG_INTERACTIONS_BACKUP', JSON.stringify(interactionsCache)); }
        catch(e) { console.error('Interactions parse error', e); }
    }
    if (typeof window.AZUMINT_SYSTEM === 'undefined') { console.log('[LBM] No system.js — showing setup'); showSetup(); return; }
    try {
        const data = JSON.parse(base64ToUnicode(window.AZUMINT_SYSTEM));
        globalAdminHash = data.adminHash; globalSalt = data.salt;
        if (data.authToken) globalAuthToken = data.authToken;
        if (data.apiKey) globalApiKey = data.apiKey;
        if (data.siteConfig) currentConfig = { ...currentConfig, ...data.siteConfig, colors: { ...currentConfig.colors, ...(data.siteConfig.colors || {}) } };

        // Load posts from posts.js or legacy fallback
        if (typeof window.AZUMINT_POSTS !== 'undefined') {
            try { postsCache = JSON.parse(base64ToUnicode(window.AZUMINT_POSTS)); console.log('[LBM] Loaded ' + postsCache.length + ' posts from posts.js'); }
            catch(e) { console.error('[LBM] posts.js parse error', e); postsCache = []; }
        } else if (data.posts && data.posts.length > 0) {
            postsCache = data.posts;
            console.log('[LBM] Legacy: ' + postsCache.length + ' posts from system.js (will migrate on login)');
        }

        // Load comics from comics.js or legacy fallback
        if (typeof window.AZUMINT_COMICS !== 'undefined') {
            try { comicsCache = JSON.parse(base64ToUnicode(window.AZUMINT_COMICS)); console.log('[LBM] Loaded ' + comicsCache.length + ' comics from comics.js'); }
            catch(e) { console.error('[LBM] comics.js parse error', e); comicsCache = []; }
        } else if (data.comics && data.comics.length > 0) {
            comicsCache = data.comics;
            console.log('[LBM] Legacy: ' + comicsCache.length + ' comics from system.js (will migrate on login)');
        }

        hideSetup(); applyVisualConfig(); renderLeaflets(); renderGallery(); renderBoards(); renderComicsGrid(); populateArtScroll();
        if (window.location.hash) { const id = parseInt(window.location.hash.substring(1)); if (!isNaN(id)) setTimeout(() => openLightbox(id), 500); }
    } catch(e) { console.error('Config parse error', e); showToast('Error reading system.js — try re-uploading', 'error'); }
}

